#include <unistd.h>

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			write(1, "-214748364", 10);
			nb = 8;
		}
		else
		{
			write(1, "-", 1);
			nb = -nb;
		}
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	if (nb >= 10)
	{
		nb = nb % 10;
	}
	nb = nb + 48;
	write(1, &nb, 1);
}
