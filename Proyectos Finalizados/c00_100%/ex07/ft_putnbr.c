#include <unistd.h>

void	ft_putnbr(int u)
{
	if (u < 0)
	{
		if (u == -2147483648)
		{
			write(1, "-214748364", 10);
			u = 8;
		}
		else
		{
			write(1, "-", 1);
			u = -u;
		}
	}
	if (u >= 10)
	{
		ft_putnbr(u / 10);
	}
	if (u >= 10)
	{
		u = u % 10;
	}
	u = u + 48;
	write(1, &u, 1);
}
